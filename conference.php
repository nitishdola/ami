            <td class="TextMain1"><div align="justify"><span class="h3">Objectives and Scope of the Symposium</span><br />
              <br />
              The uniqueness of microorganisms and their often unpredictable nature and biosynthetic capabilities in a given set of environmental and cultural conditions, has made them likely candidates for solving particularly difficult problems in life sciences and other fields as well. Microbes are one of the important bio resources that nature has bestowed on us. They carry out important activities like mobilization and recycling of nutrients as well as improvement of soil, environment, human and plant health. Rapid progress in biotechnology in recent times indicates immense potential of microbes for tackling emerging problems the humanity is facing today. Full range of microbial diversity needs to be understood properly if application of microbes for solving various environmental problems is to succeed.<br />
              <br />
              The 57th Annual Conference and International Symposium of Association of Microbiologists of India (AMI) scheduled on November 24-27, 2016 on a theme “Microbes and Biosphere: What’s New What’s Next is being organized at Guwahati, Assam, India.  The conference will be jointly organized by the Guwahati Unit of the AMI, Department of Botany, Gauhati University and the Institute of Advanced Study in Science and Technology (IASST), Guwahati, Assam, India. The conference is an attempt to provide a common forum to discuss, deliberate and propose solutions on various aspects related to the theme, with the ultimate aim of devising strategies for using microbial resources for restoring and sustaining the health of the planet earth. To fulfill the broad objectives of the conference, it will be the endeavor of the organizers to bring together leading academic scientists, researchers and research scholars from all over the country and abroad and engage them in exchanging and sharing their rich experiences and research results on different themes of the conference.<br />
