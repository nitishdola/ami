                The microorganisms influence human life immensely. The consistent challenge of food security, mitigating malnutrition, tackling environmental and health problems  are the challenges the microbiologists  need to address. Though there has been tremendous enhancement of  knowledge and technology in different fields of Microbiology, yet there are many milestones still to be achieved. Therefore, efforts are needed to understand most microbial species on the biosphere especially those present at low relative abundance. The detection and understanding of this 'rare biosphere' will enable us to solve many problems the humanity is facing today. Discussion on the ecology of rare microbial populations, besides using molecular and computational methods for targeting taxonomic 'blind spots' within the rare biosphere of complex microbial communities is the call of the time. <br />
                <br />
